# ╔═╡ 96b3c113-4a36-4f14-a20c-0dfa2cc2c194
# reads the args.txt file for a given exp_code and returns the args dict
function read_arg_data(exp_code::String; dd::String="./")::Dict{String, String}
	data_uints = read(dd * exp_code * "/args.txt")
	data_str = String(Char.(data_uints))
	data_strings = split(data_str, "\r\n")
	string_vectors = split.(data_strings, ": ")
	string_pairs = [v[1] => v[2] for v in string_vectors if length(v) == 2]
	return Dict(string_pairs)
end;

# ╔═╡ 4356e8c1-4597-45a8-a44a-5a3592406017
# reads the training_curve.csv file for a given exp_code and returns a DataFrame
function read_training_curve(exp_code::String; dd::String="./")::DataFrame
	training_curve_csv_file = dd * exp_code * "/training_curve.csv"
	CSV.read(training_curve_csv_file, DataFrame)
end;

# ╔═╡ 6b3524f8-1fb4-4000-996b-0d0f0318381d
function minimum_mses(training_curves_df)
	training_mse = training_curves_df.Training_MSE
	validation_mse = training_curves_df.Validation_MSE
	minimum(training_mse), minimum(validation_mse)
end;

# ╔═╡ 21c56b73-f725-4eac-93ed-8a0102ea4196
function get_exp_data(dd)
	# read list of folders with experimental data
	exp_codes = readdir(dd) 

	# get the experiment codes and corresponding file names
	exp_files = readdir.(dd .* exp_codes) # 2 files -> no data b/c incompatible params
	good_exp = 5 .== length.(exp_files) # sample pts that produced trained model
	good_codes = exp_codes[good_exp]
	good_files = exp_files[good_exp]
	
	# read the arg data for each experiment
	arg_data = read_arg_data.(good_codes, dd=dd)
	
	# read training curve data
	training_curves = read_training_curve.(good_codes, dd=dd)

	return DataFrame(
		:exp_code => good_codes, 
		:exp_files => good_files,
		:args => arg_data,
		:training_curves => training_curves
	)
end;

# ╔═╡ f87e51c9-19b7-4bd3-9e3a-bb56d09c07f9
