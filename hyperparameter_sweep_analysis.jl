	voro_embedding 	= parse(Int, opt_args["voro_embedding"])
	voro_h 			= parse(Int, opt_args["voro_h"])
	hidden_encoding = parse(Int, opt_args["hidden_encoding"])

	with_terminal() do 
		@info "Optimal Hyperparams" mpnn_steps voro_embedding voro_h hidden_encoding
	end
end

